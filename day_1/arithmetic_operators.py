a = 10
b = 3

# addition
print(a + b)

# subtraction
print(a - b)

# multiplication
print(a * b)

# division --> float
print(a / b)

# floor division --> int
# floor(a / b) --> int answer
print(a // b)

# power operator (a^b)
print(a ** b)

# modulus operator
# a mod b --> int remainder a / b
print(a % b)
